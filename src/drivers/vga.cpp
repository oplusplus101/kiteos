VideoGraphicsArray::VideoGraphicsArray()
    : miscPort(0x3c2),
      crtcIndexPort(0x3d4),
      crtcDataPort(0x3d5),
      sequencerIndexPort(0x3c4),
      sequencerDataPort(0x3c5),
      graphicsControllerIndexPort(0x3ce),
      graphicsControllerDataPort(0x3cf),
      attributeControllerIndexPort(0x3c0),
      attributeControllerReadPort(0x3c1),
      attributeControllerWritePort(0x3c0),
      attributeControllerResetPort(0x3da)
{
}

VideoGraphicsArray::~VideoGraphicsArray()
{
}

void VideoGraphicsArray::WriteRegisters(uint8_t *registers)
{
    //  misc
    miscPort.Write(*(registers++));

    // sequencer
    for (uint8_t i = 0; i < 5; i++)
    {
        sequencerIndexPort.Write(i);
        sequencerDataPort.Write(*(registers++));
    }

    // cathode ray tube controller
    crtcIndexPort.Write(0x03);
    crtcDataPort.Write(crtcDataPort.Read() | 0x80);
    crtcIndexPort.Write(0x11);
    crtcDataPort.Write(crtcDataPort.Read() & ~0x80);

    registers[0x03] = registers[0x03] | 0x80;
    registers[0x11] = registers[0x11] & ~0x80;

    for (uint8_t i = 0; i < 25; i++)
    {
        crtcIndexPort.Write(i);
        crtcDataPort.Write(*(registers++));
    }

    // graphics controller
    for (uint8_t i = 0; i < 9; i++)
    {
        graphicsControllerIndexPort.Write(i);
        graphicsControllerDataPort.Write(*(registers++));
    }

    // attribute controller
    for (uint8_t i = 0; i < 21; i++)
    {
        attributeControllerResetPort.Read();
        attributeControllerIndexPort.Write(i);
        attributeControllerWritePort.Write(*(registers++));
    }

    attributeControllerResetPort.Read();
    attributeControllerIndexPort.Write(0x20);
}

bool VideoGraphicsArray::SupportsMode(uint32_t width, uint32_t height, uint32_t colordepth)
{
    return (width == 320 && height == 200 && colordepth == 8) || (width == 640 && height == 480 && colordepth == 2);
}

bool VideoGraphicsArray::SetMode(uint32_t width, uint32_t height, uint32_t colordepth)
{
    if (!SupportsMode(width, height, colordepth))
        return false;

    if (width == 320 && height == 200 && colordepth == 8)
    {
        uint8_t g_320x200x256[] =
            {
                /* MISC */
                0x63,
                /* SEQ */
                0x03, 0x01, 0x0F, 0x00, 0x0E,
                /* CRTC */
                0x5F, 0x4F, 0x50, 0x82, 0x54, 0x80, 0xBF, 0x1F,
                0x00, 0x41, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
                0x9C, 0x0E, 0x8F, 0x28, 0x40, 0x96, 0xB9, 0xA3,
                0xFF,
                /* GC */
                0x00, 0x00, 0x00, 0x00, 0x00, 0x40, 0x05, 0x0F,
                0xFF,
                /* AC */
                0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07,
                0x08, 0x09, 0x0A, 0x0B, 0x0C, 0x0D, 0x0E, 0x0F,
                0x41, 0x00, 0x0F, 0x00, 0x00};
        WriteRegisters(g_320x200x256);
        this->width = width;
        this->height = height;
        this->bpp = colordepth;
        return true;
    }
    else if (width == 640 && height == 480 && colordepth == 2)
    {
        unsigned char g_640x480x16[] =
            {
                /* MISC */
                0xE3,
                /* SEQ */
                0x03, 0x01, 0x08, 0x00, 0x06,
                /* CRTC */
                0x5F, 0x4F, 0x50, 0x82, 0x54, 0x80, 0x0B, 0x3E,
                0x00, 0x40, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
                0xEA, 0x0C, 0xDF, 0x28, 0x00, 0xE7, 0x04, 0xE3,
                0xFF,
                /* GC */
                0x00, 0x00, 0x00, 0x00, 0x03, 0x00, 0x05, 0x0F,
                0xFF,
                /* AC */
                0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x14, 0x07,
                0x38, 0x39, 0x3A, 0x3B, 0x3C, 0x3D, 0x3E, 0x3F,
                0x01, 0x00, 0x0F, 0x00, 0x00};
        WriteRegisters(g_640x480x16);
        this->width = width;
        this->height = height;
        return true;
    }

    return false;
}

uint8_t *VideoGraphicsArray::GetFrameBufferSegment()
{
    graphicsControllerIndexPort.Write(0x06);
    uint8_t segmentNumber = ((graphicsControllerDataPort.Read() >> 2) & 0x03);
    switch (segmentNumber)
    {
    default:
    case 0:
        return (uint8_t *)0x00000;
    case 1:
        return (uint8_t *)0xA0000;
    case 2:
        return (uint8_t *)0xB0000;
    case 3:
        return (uint8_t *)0xB8000;
    }
}

void VideoGraphicsArray::PutPixel(int32_t x, int32_t y, uint8_t colorIndex)
{
    if (x < 0 || x >= width || y < 0 || y >= height)
        return;

    if (colorIndex == TRANSPARENT)
        return;

    uint8_t *pixelAddress = GetFrameBufferSegment() + width * y + x;
    *pixelAddress = colorIndex;
}

uint8_t VideoGraphicsArray::GetPixel(int32_t x, int32_t y)
{
    if (x < 0 || x >= width || y < 0 || y >= height)
        return 0;

    uint8_t *pixelAddress = GetFrameBufferSegment() + width * y + x;
    return *pixelAddress;
}

