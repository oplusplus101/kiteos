void VideoGraphicsArray::DrawText(char *text, int32_t x, int32_t y, uint8_t fgcolor)
{
    for (int32_t i = x, cx = x; i < text[i] != L'\0'; i++)
    {
        cx += 8;
        DrawChar(text[i], cx, y, fgcolor);
    }
}

uint8_t VideoGraphicsArray::GetColorIndex(uint8_t r, uint8_t g, uint8_t b)
{
    if (r == 0x00 && g == 0x00 && b == 0x00)
        return BLACK;
    else if (r == 0xFF && g == 0x00 && b == 0x00)
        return RED;
    else if (r == 0x00 && g == 0xFF && b == 0x00)
        return GREEN;
    else if (r == 0x00 && g == 0x00 && b == 0xFF)
        return BLUE;
    else if (r == 0xFF && g == 0xFF && b == 0xFF)
        return WHITE;
}

void VideoGraphicsArray::FillRect(int32_t x, int32_t y, int32_t width, int32_t height, uint8_t color)
{
    for (int32_t Y = y; Y < y + height; Y++)
        for (int32_t X = x; X < x + width; X++)
            PutPixel(X, Y, color);
}
